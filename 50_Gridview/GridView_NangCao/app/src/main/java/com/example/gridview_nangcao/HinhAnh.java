package com.example.gridview_nangcao;

public class HinhAnh {
    private int hinh;
    private String moTa;

    public HinhAnh(int hinh, String moTa) {
        this.hinh = hinh;
        this.moTa = moTa;
    }

    public int getHinh() {
        return hinh;
    }

    public void setHinh(int hinh) {
        this.hinh = hinh;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }
}
